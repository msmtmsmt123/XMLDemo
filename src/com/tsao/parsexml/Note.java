package com.tsao.parsexml;

public class Note {
	
	public  String title;
    public  String body;
    
    

	public void setTitle(String title) {
		this.title = title;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getTitle() {
		return title;
	}

	public String getBody() {
		return body;
	}

}
